package voPackage;

public class SophosInfoVO {
	private String engineVersion;
	private String savVersion;
	private String LibraryVerson;
	private String numOfDetectableVirus;
	private String dateOfVirusData;
	
	public String getEngineVersion() {
		return engineVersion;
	}
	public void setEngineVersion(String engineVersion) {
		this.engineVersion = engineVersion;
	}
	public String getSavVersion() {
		return savVersion;
	}
	public void setSavVersion(String savVersion) {
		this.savVersion = savVersion;
	}
	public String getLibraryVerson() {
		return LibraryVerson;
	}
	public void setLibraryVerson(String libraryVerson) {
		LibraryVerson = libraryVerson;
	}
	public String getNumOfDetectableVirus() {
		return numOfDetectableVirus;
	}
	public void setNumOfDetectableVirus(String numOfDetectableVirus) {
		this.numOfDetectableVirus = numOfDetectableVirus;
	}
	public String getDateOfVirusData() {
		return dateOfVirusData;
	}
	public void setDateOfVirusData(String dateOfVirusData) {
		this.dateOfVirusData = dateOfVirusData;
	}
	
}
