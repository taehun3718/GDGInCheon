package com.sqisoft.ssbr.inventory.vo;

public class InventoryCategoryVO {
	private int		inventCatSeqno;
	private String	inventCatName;
	private String	inventCatCode;
	
	public int getInventCatSeqno() {
		return inventCatSeqno;
	}
	public void setInventCatSeqno(int inventCatSeqno) {
		this.inventCatSeqno = inventCatSeqno;
	}
	public String getInventCatName() {
		return inventCatName;
	}
	public void setInventCatName(String inventCatName) {
		this.inventCatName = inventCatName;
	}
	public String getInventCatCode() {
		return inventCatCode;
	}
	public void setInventCatCode(String inventCatCode) {
		this.inventCatCode = inventCatCode;
	}
	
	
}
